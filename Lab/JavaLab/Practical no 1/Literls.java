public class Literls {
    public static void main(String[] args) {
        int p=7;
        int n=-8;
        int  h=0xAB3;
        int o=0743;
        int d=981;
        int b=0b01101;
        System.out.println("Positive integer literals :"+p);
        System.out.println("Negative integer literals :"+n);
        System.out.println("Hexadecimal Literals :"+h);
        System.out.println("Octal Literals :"+o);
        System.out.println("Decimal Literals :"+d);
        System.out.println("Binary Literals :"+b);
    }
}