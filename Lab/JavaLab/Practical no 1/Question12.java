//WAP to print even number from 300 to 500
public class Question12 {
    public static void main(String[] args) {
        int i=300;
        System.out.println("Even number from 300 to 500 are :");
        for(i=300;i<500;i++){
            if (i%2==0){
                System.out.print(i+", ");
            }
        }
    }
    
}
